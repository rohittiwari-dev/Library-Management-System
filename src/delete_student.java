    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        if(jTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Please fill student Id");
        }
        else
        {
            String url="Select sname,fname,branch,course,semester from add_student where studentid=?";
            try {
                ps=conn.prepareStatement(url);
                ps.setString(1, jTextField1.getText());
                rs=ps.executeQuery();
                if(rs.next())
                {
                    jTextField2.setText(rs.getString(1));
                    jTextField3.setText(rs.getString(2));
                    jTextField4.setText(rs.getString(3));
                    jTextField5.setText(rs.getString(4));
                    jTextField6.setText(rs.getString(5));
                    ImageIcon icon=new ImageIcon("Assets\\verify.png");
                    jLabel3.setIcon(icon);

                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "No Student Registered with this Student ID: "+jTextField1.getText());
                    jLabel3.setIcon(null);
                }
            } catch (Exception e) {
            }
            finally
            {
                try {
                    rs.close();
                    ps.close();
                } catch (Exception e) {
                }
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

