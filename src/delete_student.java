    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().equals("")||jTextField2.getText().equals("")||jTextField3.getText().equals("")||jTextField4.getText().equals("")||jTextField5.getText().equals("")||jTextField6.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Please fill The Complete Information");
        }
        else
        {
            String url="Delete from add_student WHERE studentid=?";
            try {
                ps=conn.prepareStatement(url);
                ps.setString(1, jTextField1.getText());
                ps.execute();
                JOptionPane.showMessageDialog(rootPane, "Student with this Id: "+jTextField1.getText()+" is Deleted on Your Request");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField1.requestFocus();
                jLabel3.setIcon(null);
                jTextField1.setText("");

            } catch ( SQLException e)
            {
                JOptionPane.showMessageDialog(rootPane, e);
            }
            finally
            {
                try {
                    ps.close();
                } catch (Exception e) {
                }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

