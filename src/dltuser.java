    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int flag=0,flag1=0;
        String name="";
        if(jTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Please Fill The Username");
        }
        else
        {
            String sql="select username from user_reg where username=? and password=?";
            try {
                ps=conn.prepareStatement(sql);
                ps.setString(1, jTextField1.getText());
                ps.setString(2, jTextField3.getText());
                rs=ps.executeQuery();
                if(rs.next())
                {
                    flag=1;
                    name=rs.getString(1);
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "Incorect Password or Username");
                }
            } catch (SQLException ex) {
                    Logger.getLogger(User_reg.class.getName()).log(Level.SEVERE, null, ex);
            }
            finally
            {
                try
                {
                    rs.close();
                    ps.close();
                }
                catch (SQLException ex)
                {
                    Logger.getLogger(User_reg.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        if(flag==1)
        {
            String sql="Delete from user_reg where username=?";
            try {
                ps=conn.prepareStatement(sql);
                ps.setString(1, name);
                ps.execute();
                JOptionPane.showMessageDialog(rootPane, "Your Account Has been Deleted on Your Request");
                flag1=1;
            } catch (SQLException ex) {
                Logger.getLogger(dltuser.class.getName()).log(Level.SEVERE, null, ex);
            }finally
            {
                try
                {
                    ps.close();
                }
                catch (SQLException ex)
                {
                    Logger.getLogger(User_reg.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        if(flag1==1)
        {
            String sql="Select count(*) from user_reg";
            try {
                ps=conn.prepareStatement(sql);
                rs=ps.executeQuery();
                rs.next();
                int cnt=rs.getInt(1);
                if(cnt!=0)
                {
                    Login ln=new Login();
                    ln.setVisible(true);
                    this.dispose();
                }
                else{
                    User_reg ur=new User_reg();
                    ur.setVisible(true);
                    ur.jButton3.setVisible(false);
                    ur.jButton2.setVisible(false);
                    ur.jButton4.setVisible(false);
                    this.dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(dltuser.class.getName()).log(Level.SEVERE, null, ex);
            }finally
            {
                try
                {
                    ps.close();
                }
                catch (SQLException ex)
                {
                    Logger.getLogger(User_reg.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

