    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jTextField5.setText("");
        jTextField6.setText("");
        if(jTextField4.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Please fill Book Id");
        }
        else
        {
            String url="Select bookname,edition,quantity from add_book where bookid=?";
            try {
                ps=conn.prepareStatement(url);
                ps.setString(1, jTextField4.getText());
                rs=ps.executeQuery();
                if(rs.next())
                {
                    if(rs.getInt(3)==0)
                    {
                        JOptionPane.showMessageDialog(rootPane, "The Book is Out of Stock Registerd with Book Id: "+jTextField4.getText());
                    }
                    else
                    {
                        jTextField5.setText(rs.getString(1));
                        jTextField6.setText(rs.getString(2));
                        ImageIcon icon=new ImageIcon("Assets\\verify.png");
                        jLabel4.setIcon(icon); 
                    }

                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "No Book Registered with this Book ID: "+jTextField4.getText()+" or it is Out of Stock");
                    jLabel4.setIcon(null);
                }
            } catch (Exception e) {
            }
            finally
            {
                try {
                    rs.close();
                    ps.close();
                } catch (Exception e) {
                }
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

