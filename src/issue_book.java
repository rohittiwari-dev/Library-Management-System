    public void searchdet(String ref)
    {
        try {
            String s="Select quantity from add_book where bookid=?";
            ps=conn.prepareStatement(s);
            ps.setString(1, ref);
            rs=ps.executeQuery();
            rs.next();
            if(Integer.parseInt(rs.getString(1))==0)
                quan=Integer.parseInt(rs.getString(1));
            else
                quan=Integer.parseInt(rs.getString(1))-1;
            System.err.println(quan);
        } catch (Exception e) {
        }
        finally
        {
            try {
                rs.close();
                ps.close();
            } catch (Exception e) {
            }
        }
    }
    public void return_book(String ref)
    {
        try {
            String s="UPDATE add_book SET quantity="+quan+" where bookid=?";
            ps=conn.prepareStatement(s);
            ps.setString(1, ref);
            ps.executeUpdate();
            quan=0;
        } catch (Exception e) {
        }
        finally
        {
            try {
                rs.close();
                ps.close();
            } catch (Exception e) {
            }
        }
    }
    public int verify(String bookid,String studntid)
    {
        int i=0;
        String url="Select bookname from issue_book where bookid=? and studntid=?";
            try {
                ps=conn.prepareStatement(url);
                ps.setString(1, bookid);
                ps.setString(2, studntid);
                rs=ps.executeQuery();
                if(rs.next())
                {
                    jButton6.doClick();
                    JOptionPane.showMessageDialog(rootPane, "Book with this "+bookid+" Book Id Already Issued to same Student with Id "+studntid);
                    
                }
                else
                    i=1;
            } catch (Exception e) {
            }
            finally
            {
                try {
                    rs.close();
                    ps.close();
                } catch (Exception e) {
                }
            }
        return i;
    }
